//CONVERSION FLOAT NUMBER INTO INT

#include<stdio.h>
int main()
{
    float n1;
    int n2;

    printf("\n\n       CONVERSION OF FLOAT NUMBER INTO INT NUMBER :\n       ------------------------------------------");
    printf("\n\n\t      Enter the value of n1 : ");
    scanf("%f",&n1);

    n2= n1;
    printf("\n\n\t      The int value of n2 is %d",n2);

    printf("\n\n\n      Successfully Executed...\n\n\n");
    return 0;
}
