//Area of Triangle
#include<stdio.h>
int main()
{
    float base, height,area;

    printf("\n\t Enter the value of base and height: ");
    scanf("%f%f",&base,&height);

    area=(base*height)/2;

    printf("\n\t Area of Triangle:%f",area);

    printf("\n\n\t Successfully Executed...\n\n");
    return 0;


}
